package model;

public class Calculator{
    public double sum(double num1, double num2){
        return num1+num2;
    }
    public double minus(double num1, double num2){
        return num1-num2;
    }
    public double mult(double num1, double num2){
        return num1*num2;
    }
    public double div(double num1, double num2){
        return num1/num2;
    }
}
